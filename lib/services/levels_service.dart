class LevelsService {
  static final LevelsService _instance = LevelsService._();

  LevelsService._();

  static LevelsService get instance => _instance;

  Future<List<Level>> getAllLevels() async {
    try {
      final data = await Supabase.instance.client
          .from('levels')
          .select()
          .order('id', ascending: true);

      kLog.wtf(data);
      if (data.isNotEmpty) {
        return data.map((e) => Level.fromJson(e)).toList();
      }
      return [];
    } catch (e) {
      kLog.e(e);
      return [];
    }
  }

  Future<Level?> getLevel(int level) async {
    try {
      final data = await Supabase.instance.client
          .from('levels')
          .select()
          .eq('level', level)
          .single();

      kLog.wtf(data);
      if (data != null) {
        return Level.fromJson(data);
      }
      return null;
    } catch (e) {
      kLog.e(e);
      return null;
    }
  }

  Future<void> addGroupedWords(Level groupedWords) async {
    try {
      await Supabase.instance.client.from('levels').insert({
        'words': groupedWords.words,
        'letters': groupedWords.letters,
        'level': groupedWords.level,
      });
    } catch (e) {
      kLog.e(e);
    }
  }

  Future<void> addGroupedWordsFromMap() async {
    try {
      var level = 1;

      List<String> sortedWords = words.keys.toList()
        ..sort(
          (a, b) => a.length.compareTo(b.length),
        );

      Map<String, dynamic> sortedMap = {
        for (var key in sortedWords) key: words[key]
      };

      sortedMap.forEach((key, value) async {
        kLog.wtf('Add $key');
        await Supabase.instance.client.from('levels').insert({
          'words': value,
          'letters': key.split(''),
          'level': level++,
        });
      });
    } catch (e) {
      kLog.e(e);
    }
  }
}

class Level {
  final int id;
  final int level;

  final Set<String> words;
  final Set<String> letters;

  Level({
    required this.id,
    required this.level,
    required this.words,
    required this.letters,
  });

  factory Level.fromJson(Map<String, dynamic> json) {
    return Level(
      id: json['id'] as int,
      level: json['level'] as int,
      words: Set<String>.from(json['words'] as List<dynamic>),
      letters: Set<String>.from(json['letters'] as List<dynamic>),
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'words': words,
      'letters': letters,
      'level': level,
    };
  }
}
